const typeDefs = gql`
  type Book {
    title: String!
    published: Int!
    author: String!
    id: ID!
    genres: [String!]!
  }

  type Author {
    name: String!
    born: Int
    id: ID!
    bookCount: Int
  }

  type Query {
    bookCount: Int!
    authorCount: Int!
    allBooks(name: String, genre: String): [Book!]!
    allAuthors: [Author!]!
  }

  type Mutation {
    addBook(
      title: String!
      author: String!
      published: Int!
      genres: [String!]!
    ): Book

    editAuthor(
      name: String!
      born: Int
    ): Author
  }
`

const resolvers = {
  Query: {
    bookCount: () => books.length,
    authorCount: () => authors.length,
    allAuthors: () => authors,
    allBooks: (root, args) => {
      const author = args.name
      const genre = args.genre

      if (!author && !genre) {
        return null
      }

      if (!author && genre) {
        return books.filter(it => it.genres.includes(genre))
      }

      return books.filter(it => genre ? (
        it.author === author &&
          it.genres.includes(genre)
        ): it.author === author)
    }
  },

  Author: {
    bookCount: (root) => {
      return books.filter(it => 
        it.author === root.name).length
    }
  },

  Mutation: {
    addBook: (root, args) => {
      const update = {...args, id: uuid()}
      const isNewAuthor = !books.some(it => 
        it.author === update.author)
     
      if (isNewAuthor) {
        authors = authors.concat({name: update.author, id: uuid()})
      }

      books = books.concat(update)

      return {
        title: update.title,
        author: update.author
      }
    },
    editAuthor: (root, args) => {
      const update = {...args, id: uuid()}
      const selectedAuthor = authors.find(it =>
        it.name === update.name)
      
      if (!selectedAuthor) {
        return null
      }

      authors = authors.map(it => it.name === update.name ?
        update : it)
      return update
    }
  }
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

server.listen().then(({ url }) => {
  console.log(`Server ready at ${url}`)
})